public class Main {
    public static void main(String[] args) {
        System.out.println("Hello Java");
        System.out.println("4"+"4");
        System.out.println(4+4+"4"+4+4);
        System.out.println("4"+4);
        System.out.println("4"+(4+4));
        System.out.println("ab"+5+5);
    }
}
/* Java is OOP everything must be enclosed within a class.

class name and file name should be same and class name is used to find the file. if name different then program will not be compiled.

Every statement is terminated using semicolon ;

java is strongly typed hence types must be declared beforehand.

System is a class and out stands for output.

println() stands for print line, this method automatically adds the newline char "\n" to the end.

print() doesn't used the newline char

*/